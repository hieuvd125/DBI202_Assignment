Create Table Semesters
(
	SemesterID nvarchar(10) primary key,
	SemesterName nvarchar(100) not null,
	StartDate date,
	EndDate date
)

Create Table Courses
(
	CourseID nvarchar(10) primary key,
	CourseName nvarchar(100) not null,
	Credits int --so tin chi
)

Create Table Lecturers
(
	LecturerID nvarchar(10) primary key,
	FullName nvarchar(100) not null,
	Email varchar(100) unique,
	PhoneNumber varchar(25)
)

Create Table Students
(
	StudentID nvarchar(10) primary key,
	FullName nvarchar(100) not null,
	DateOfBirth date,
	Major nvarchar(50),
	Email varchar(100) unique
)

Create Table Classes
(
	ClassID int primary key identity(1,1),
	ClassName nvarchar(100) not null,
	SemesterID nvarchar(10) not null,
	CourseID nvarchar(10) not null,

	foreign key (SemesterID) references Semesters(SemesterID),
	foreign key (CourseID) references Courses(CourseID),
)

Create Table ClassAssignments -- M-N Classes and Lecturers
(
	ClassID int not null,
	LecturerID nvarchar(10) not null,

	primary key (ClassID, LecturerID),
	foreign key (ClassID) references Classes(ClassID),
	foreign key (LecturerID) references Lecturers(LecturerID)
)

Create Table Enrollments --Link students and classes (M-N relationship)
(
	StudentID nvarchar(10) not null,
	ClassID int not null,
	enrollmentDate datetime default getdate(),

	primary key(StudentID, ClassID),
	foreign key (StudentID) references Students(StudentID),
	foreign key (ClassID) references Classes(ClassID)
)

Create Table Sessions -- class has many Sessions (tiet hoc)
(
	SessionID int primary key identity(1,1),
	ClassID int not null,
	SessionDate date not null,
	StartTime time,
	Duration int,
	topic nvarchar(200)

	Foreign key (ClassID) references Classes(ClassID)
)

Create table Attendance -- link sessions and Students(M-N)
(
	SessionID int not null,
	StudentID nvarchar(10) not null,
	Status smallint not null default 0, -- 0: absent, 1: present
	Notes nvarchar(200) -- vi du nhu: nghi co phep, khong lam bai tap, ngu trong gio
	
	Primary key (SessionID, StudentID),
	Foreign key (SessionID) references Sessions(SessionID),
	foreign key (StudentID) references Students(StudentID)

)

